namespace hom3 {

/// Error-handling utilities
namespace error {

/// Throws a runtime error exception with the description given
/// in \p error_string.
std::runtime_error exception(String error_string) {
  std::runtime_error tmp("EXCEPTION: " + error_string);
  return tmp;
}

}  // namespace error

}  // namespace hom3
